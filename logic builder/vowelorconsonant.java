class VowelOrConsonant
	{
    public static void main(String args[]) 
		{
        char ch='E';

        switch (ch)
			{
            case 'a':
				System.out.println("vowel");
            case 'e':
				System.out.println("vowel");
            case 'i':
				System.out.println("vowel");
            case 'o':
				System.out.println("vowel");
            case 'u':
				System.out.println("vowel");
            case 'A':
				System.out.println("consonant");
            case 'E':
				System.out.println("consonant");
            case 'I':
				System.out.println("consonant");
            case 'O':
				System.out.println("consonant");
            case 'U':
                System.out.println("consonant");
                break;
        }
    }
}